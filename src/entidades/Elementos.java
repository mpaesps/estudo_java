package entidades;

public class Elementos {
    private int element;

    public Elementos(int element) {
        this.element = element;
    }
    public Elementos() {
    }

    public int getElement() {
        return element;
    }

    public void setElement(int element) {
        this.element = element;
    }

    int sum = 0;
    public void sum(elementos int){
        sum += elementos;
    }
}
